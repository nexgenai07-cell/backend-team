from django.db import models
class EvaluationSession(models.Model):
    """
    [Main Table / Parent Model]
    Created whenever a user submits a new prompt. 
    It tracks the overall session details and the ultimate winner.
    """
    # Stores the user's input prompt/query
    prompt = models.TextField()
    
    # Stores the name of the winning model (Updated post-evaluation; null initially)
    winner_model = models.CharField(max_length=100, blank=True, null=True)
    
    # Stores the score achieved by the winning model (null initially)
    winner_score = models.FloatField(blank=True, null=True)
    created_at = models.DateTimeField(auto_now_add=True)
    def __str__(self):
        return f"Session {self.id} — {self.prompt[:50]}"
    class Meta:
        ordering = ['-created_at']
class ModelResponse(models.Model):
    """
    [Sub-Table 1 / Child of EvaluationSession]
    Stores the individual response generated by each LLM for a specific session.
    """
    # ForeignKey relation: If an EvaluationSession is deleted, all its associated responses are deleted (CASCADE).
    # Allows fetching all responses for a session via `session.responses.all()`.
    session = models.ForeignKey(
        EvaluationSession,
        on_delete=models.CASCADE,
        related_name='responses'
    )
    model_name = models.CharField(max_length=100)
    # Anonymous placeholder shown to judges to prevent bias (e.g., "A", "B", "C")
    anonymous_label = models.CharField(max_length=5)
    response_text = models.TextField()
    response_time_ms = models.FloatField()
    # Calculated average score from all judges (null until evaluation is complete)
    final_score = models.FloatField(blank=True, null=True)
    created_at = models.DateTimeField(auto_now_add=True)

    def __str__(self):  # Admin panel representation: e.g., "Meta-Llama-3 — Session 5"
        return f"{self.model_name} — Session {self.session.id}"

    class Meta:
        ordering = ['anonymous_label']


class JudgeScore(models.Model):
    """
    [Sub-Table 2 / Child of ModelResponse]
    Stores the individual score given by a specific AI Judge (e.g., Gemini, GPT) for a single model response.
    """
    # ForeignKey relation: If a ModelResponse is deleted, its corresponding judge scores are also deleted.
    # Allows fetching all scores for a response via `response.judge_scores.all()`.
    model_response = models.ForeignKey(
        ModelResponse,
        on_delete=models.CASCADE,
        related_name='judge_scores'
    )
    judge_name = models.CharField(max_length=100)
    score = models.FloatField()
    created_at = models.DateTimeField(auto_now_add=True)
    def __str__(self):
        return f"{self.judge_name} → {self.model_response.anonymous_label}: {self.score}"